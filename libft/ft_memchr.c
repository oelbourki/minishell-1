#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	i = 0;
	tmp = (unsigned char*)s;
	while (i < n && tmp[i] != (unsigned char)c)
		i++;
	if (i == n)
		return (NULL);
	tmp += i;
	return (tmp);
}
