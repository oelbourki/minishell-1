#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*f;

	i = 0;
	f = (char*)s;
	while (i < n)
	{
		*f = (unsigned char)0;
		i++;
		f++;
	}
}
